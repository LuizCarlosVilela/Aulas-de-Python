# - Criando primeira variável :)

# nomeVariável = valorQueElaVaiReceber

#Pessoa é o nome da variável
#Luiz é o valor dela

pessoa = "Luiz" #Essa variável é do tipo String = Texto

#idade é o nome da variável
#16 é o valor dela
idade = 16 #Essa variável é do tipo int = inteiro = número normais

#altura é o nome da variável
#1.8 é o valor dela
altura = 1.8 #Essa variável é do tipo float = números decimais

#Para mostrar uma variável

print(pessoa)
print(idade)
print(altura)

print(type(pessoa))

#Alterando valor

pessoa = "Roberto"
print(pessoa)

#Alterando tipo da variável
